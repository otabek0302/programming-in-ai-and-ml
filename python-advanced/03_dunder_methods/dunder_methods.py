# =============================================================
# PYTHON DUNDER (MAGIC) METHODS
# =============================================================

# Dunder = double underscore (__method__)
# Python calls these automatically in response to built-in operations.
# They let your objects integrate with Python's syntax and built-ins.

# =============================================================
# OBJECT LIFECYCLE
# =============================================================

class MyClass:
    def __new__(cls, *args, **kwargs):
        """Called BEFORE __init__. Creates and returns the instance."""
        print(f"  __new__ called for {cls.__name__}")
        instance = super().__new__(cls)
        return instance

    def __init__(self, value: int):
        """Called AFTER __new__. Initialises the instance."""
        print(f"  __init__ called with value={value}")
        self.value = value

    def __del__(self):
        """Called when the object is about to be garbage-collected."""
        print(f"  __del__ called for value={self.value}")


obj = MyClass(42)
del obj

