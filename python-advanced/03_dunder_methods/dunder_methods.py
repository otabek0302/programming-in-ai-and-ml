class Vector:
    def __init__(self, x: float, y: float):
        self.x = x
        self.y = y

    def __str__(self) -> str:
        """Human-readable. Called by print() and str()."""
        return f"Vector({self.x}, {self.y})"

    def __repr__(self) -> str:
        """Unambiguous. Called in REPL, repr(), and inside containers."""
        return f"Vector({self.x!r}, {self.y!r})"

    def __format__(self, spec: str) -> str:
        """Called by format() and f-strings with format spec."""
        if spec == "polar":
            import math
            r = math.hypot(self.x, self.y)
            theta = math.degrees(math.atan2(self.y, self.x))
            return f"({r:.2f} ∠ {theta:.1f}°)"
        return f"({self.x:{spec}}, {self.y:{spec}})"


v = Vector(3, 4)
print(str(v))       # Vector(3, 4)
print(repr(v))      # Vector(3, 4)
print(f"{v}")       # Vector(3, 4)
print(f"{v:.2f}")   # (3.00, 4.00)
print(format(v, "polar"))   # (5.00 ∠ 53.1°)

