# ATTRIBUTE ACCESS
# =============================================================

class AttrLogger:
    def __init__(self, **kwargs):
        self.__dict__.update(kwargs)

    def __getattr__(self, name: str):
        """Called only when normal attribute lookup fails."""
        print(f"  __getattr__: '{name}' not found, returning default")
        return None

    def __setattr__(self, name: str, value):
        """Called on EVERY attribute assignment."""
        print(f"  __setattr__: {name} = {value!r}")
        super().__setattr__(name, value)

    def __delattr__(self, name: str):
        print(f"  __delattr__: {name}")
        super().__delattr__(name)


obj = AttrLogger(x=1, y=2)
print(obj.x)        # 1
print(obj.z)        # __getattr__ – z not found
obj.z = 99          # __setattr__
del obj.z           # __delattr__

