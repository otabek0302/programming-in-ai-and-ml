# HASHING  (__hash__)
# =============================================================

# Objects used as dict keys or set members must be hashable.
# If you define __eq__, Python sets __hash__ = None (unhashable).
# Define __hash__ explicitly to keep hashability.

class Point:
    def __init__(self, x: int, y: int):
        self.x = x
        self.y = y

    def __eq__(self, other: object) -> bool:
        if not isinstance(other, Point):
            return NotImplemented
        return (self.x, self.y) == (other.x, other.y)

    def __hash__(self) -> int:
        return hash((self.x, self.y))

    def __repr__(self) -> str:
        return f"Point({self.x}, {self.y})"


p1 = Point(1, 2)
p2 = Point(1, 2)
p3 = Point(3, 4)

print(p1 == p2)     # True
print(hash(p1) == hash(p2))  # True

points = {p1, p2, p3}   # works because __hash__ is defined
print(points)            # {Point(1, 2), Point(3, 4)}

d = {p1: "home", p3: "office"}
print(d[p2])     # "home" – p2 equals p1, same hash

