# Python is a popular programming language created by Guido van Rossum, released in 1991.

# --- What is Python used for? ---
# - Web development (server-side)
# - Software development
# - Mathematics
# - System scripting

# --- What can Python do? ---
# - Create web applications on a server
# - Work alongside software to create workflows
# - Connect to databases, read and modify files
# - Handle big data and perform complex mathematics
# - Rapid prototyping or production-ready software development

# --- Why Python? ---
# - Works on different platforms (Windows, Mac, Linux, Raspberry Pi, etc.)
# - Simple syntax similar to the English language
# - Fewer lines of code compared to other languages
# - Interpreter system: code executes as soon as it is written
# - Can be used in procedural, object-oriented, or functional style

# --- Good to know ---
# We use Python 3 in this tutorial.
# IDEs: Thonny, PyCharm, Netbeans, Eclipse, VS Code

# --- Python vs other languages ---
# Python uses NEW LINES to complete a command (not semicolons or parentheses)
# Python uses INDENTATION (whitespace) to define scope (not curly-brackets)

