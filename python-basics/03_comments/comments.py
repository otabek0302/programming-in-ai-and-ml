# =============================================================
# PYTHON COMMENTS
# =============================================================

# Comments explain the code and are ignored by Python.
# Uses: explain code, improve readability, disable code during testing.

# --- Single-line comment ---
# This is a comment
print("Hello, World!")  # inline comment after code

# --- Multi-line comment (triple quotes) ---
# Python has no official multi-line comment syntax.
# Triple-quoted strings that are NOT assigned to a variable are ignored at runtime.
"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")

# You can also use multiple # lines.
# Line 1 of comment
# Line 2 of comment
# Line 3 of comment

# --- Disabling code for testing ---
# print("This line is disabled")
print("This line runs")
